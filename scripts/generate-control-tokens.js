#!/usr/bin/env node
const { readFileSync, writeFileSync, mkdirSync } = require("node:fs");
const { join } = require("node:path");

const root = join(__dirname, "..");
const tokensPath = join(root, "docs/global-design/control-rules/tokens/control.tokens.json");
const outDir = join(root, "packages/ui/src/generated");

function tokenKeyToCssVar(key) {
  return `--${key.replace(/\./g, "-")}`;
}

const raw = JSON.parse(readFileSync(tokensPath, "utf8"));
const presetId = raw.meta.defaultColorPresetId;
const preset = raw.colorPresets[presetId];
if (!preset || !preset.colors) {
  console.error(`Missing color preset: ${presetId}`);
  process.exit(1);
}

const merged = { ...preset.colors, ...raw.tokens };
const keys = Object.keys(merged).sort((a, b) => a.localeCompare(b));

if (keys.length === 0) {
  console.error("No tokens to generate");
  process.exit(1);
}

const header = "/* DO NOT EDIT — generated by scripts/generate-control-tokens.js */\n";

const tsLines = [
  header.trim(),
  "",
  "export const CONTROL_TOKENS = {",
  ...keys.map((key) => `  ${JSON.stringify(key)}: ${JSON.stringify(merged[key])},`),
  "} as const;",
  "",
  "export type ControlTokenKey = keyof typeof CONTROL_TOKENS;",
  "",
  "export const CONTROL_TOKEN_KEYS = Object.keys(CONTROL_TOKENS) as ControlTokenKey[];",
  "",
];

const cssLines = [
  header.trim(),
  ":root {",
  ...keys.map((key) => `  ${tokenKeyToCssVar(key)}: ${merged[key]};`),
  "}",
  "",
];

mkdirSync(outDir, { recursive: true });
writeFileSync(join(outDir, "control-tokens.ts"), tsLines.join("\n"));
writeFileSync(join(outDir, "control-tokens.css"), cssLines.join("\n"));

console.log(`Generated ${keys.length} tokens from ${raw.schemaVersion} (preset: ${presetId})`);
